function getRandomQuote(category) {
    const categoryQuotes = quotes[category];
    if (!categoryQuotes) return "Цитаты не найдены.";
    const randomIndex = Math.floor(Math.random() * categoryQuotes.length);
    return categoryQuotes[randomIndex];
}

document.addEventListener("DOMContentLoaded", function () {
    const savedCategory = localStorage.getItem('lastCategory');
    if (savedCategory) {
        document.getElementById('category').value = savedCategory;
    }

    document.getElementById("get-quote").addEventListener("click", function () {
        const category = document.getElementById("category").value;
        const quoteText = getRandomQuote(category);
        document.getElementById("quote").innerText = quoteText;
    });

    document.getElementById("category").addEventListener("change", function () {
        localStorage.setItem('lastCategory', document.getElementById('category').value);
    });

    document.getElementById("add-quote").addEventListener("click", function () {
        alert("Функция добавления цитаты в разработке!");
    });

    document.getElementById("share-quote").addEventListener("click", function () {
        const quoteText = document.getElementById('quote').innerText;
        if (!quoteText) {
            alert("Сначала получите цитату!");
            return;
        }
        navigator.clipboard.writeText(quoteText).then(() => {
            alert("Цитата скопирована! Можно вставить в соцсеть.");
        });
    });
});
